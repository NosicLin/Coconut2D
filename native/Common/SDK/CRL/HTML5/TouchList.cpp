#include "TouchList.hpp"

////////////////////////////////////////////////////////////////////////////////////////////////////
TouchList::TouchList()
{
	for(int i = 0; i <= 5; i++)
	{
		Touch* touch = new Touch();
		touch->initTouch(i, nullptr, 0, 0, 0, 0, 0, 0);
		this->push(touch);
	}
}

////////////////////////////////////////////////////////////////////////////////////////////////////
Touch* TouchList::item(int index)
{
	return (*this)[index];
}
