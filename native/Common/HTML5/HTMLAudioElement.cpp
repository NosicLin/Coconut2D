#include "HTMLAudioElement.hpp"

////////////////////////////////////////////////////////////////////////////////////////////////////
Audio::Audio(String url)
{
	src = url;
	stream = new fxAudioStream(src.c_str());
}

////////////////////////////////////////////////////////////////////////////////////////////////////
Audio::~Audio()
{
	delete stream;
	trace("Delete Audio " + src);
}

////////////////////////////////////////////////////////////////////////////////////////////////////
void Audio::play()
{
	if(stream)
		stream->play();
	//__Audio_play(__uid);
}

////////////////////////////////////////////////////////////////////////////////////////////////////
void Audio::pause()
{
	if(stream)
		stream->stop();
	//__Audio_pause(__uid);
}
