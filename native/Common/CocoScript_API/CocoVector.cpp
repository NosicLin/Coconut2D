#include "CocoVector.hpp"

////////////////////////////////////////////////////////////////////////////////////////////////////
CocoVector::CocoVector()
{
	X = 0;
	Y = 0;
	Z = 0;
	W = 1;
}

////////////////////////////////////////////////////////////////////////////////////////////////////
void CocoVector::set(float x, float y, float z, float w)
{
	X = x;
	Y = y;
	Z = z;
	W = w;
}
