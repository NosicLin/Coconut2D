#include "StateAnim.hpp"

////////////////////////////////////////////////////////////////////////////////////////////////////
StateAnim::StateAnim()
{
	scene = new Animation1();
}

////////////////////////////////////////////////////////////////////////////////////////////////////
void StateAnim::tick(CocoEngine* engine, WebGLRenderingContext* gl, float time)
{
	super->tick(engine, gl, time);
	if(scene)
	{
		scene->tick(gl, time);
	}
}
